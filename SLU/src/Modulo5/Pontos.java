package Modulo5;

public class Pontos {
	private double pontoX;
	private double pontoY;
	
	public Pontos() {}
	
	public Pontos(double pontoX, double pontoY)
	{
		this.pontoX = pontoX;
		this.pontoY = pontoY;
	}
	
	public double getPontoX() {
		return pontoX;
	}
	public void setPontoX(double pontoX) {
		this.pontoX = pontoX;
	}
	public double getPontoY() {
		return pontoY;
	}
	public void setPontoY(double pontoY) {
		this.pontoY = pontoY;
	}
	
	
}
